     public void createArray(){
        deleteArrays();
        num = (Integer)spSoPT.getValue();
        lbArrays = new JLabel[num];
        txtArrays = new JTextField[num];
        arrays = new int [num];
        
        for(int i=0; i<num; i++){
            lbArrays[i] = new JLabel("A"+i);
            txtArrays[i] = new JTextField();
            
            add(lbArrays[i]);
            add(txtArrays[i]);
            
            lbArrays[i].setSize(40,30);
            lbArrays[i].setFont(new Font("Tahoma", Font.BOLD, 18));

            if((0<=i) && (i<=14)){
                lbArrays[i].setLocation(180*(i+1)/3, 80);
                
            }
            txtArrays[i].setSize(50,30);
            txtArrays[i].setLocation(lbArrays[i].getX()-10, lbArrays[i].getY()+30);
        }
        setVisible(true);
        validate();
        repaint();
    }
    
   public void deleteArrays(){
        for(int i=0; i<num; i++){
            lbArrays[i].setVisible(false);
            txtArrays[i].setVisible(false);
            remove(lbArrays[i]);
            remove(txtArrays[i]);
        }
    }
