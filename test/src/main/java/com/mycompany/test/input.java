    private void btnXacnhanActionPerformed(java.awt.event.ActionEvent evt) {   
        Frame[] listFrames = Frame.getFrames();
        for(int i=0; i<num; i++){ 
            if("".equals(txtArrays[i].getText())){
                JOptionPane.showMessageDialog(null, "Phần tử A"+i+" bị bỏ trống!");
            }
            arrays[i] = (int) Integer.parseInt(txtArrays[i].getText());
            System.out.println(arrays[i]);
        }
        
        ((Main)listFrames[0]).setArray(arrays);
        ((Main)listFrames[0]).Filewriter(arrays, num);

        if(num!=0){
            JOptionPane.showMessageDialog(null, "Tạo dữ liệu mảng thành công!");
            dispose();  
        }
        else {
            JOptionPane.showMessageDialog(null, "Tạo dữ liệu mảng không thành công!");
            dispose();
        }
       

                                          
        
    }                                        

